package com.kh.app11.menu.model.vo;

public class menuVo {
	private String menu;
	private String price;
	

	
	public menuVo() {
		this.menu = menu;
		this.price = price;
	}
	public String getMenu() {
		return menu;
	}
	public void setMenu(String menu) {
		this.menu = menu;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	
	
	
}
