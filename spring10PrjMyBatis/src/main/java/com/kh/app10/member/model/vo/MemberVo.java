package com.kh.app10.member.model.vo;


public class MemberVo {
	private String id;
	private String pwd;
	private String nink;
	
	
	
	public MemberVo() {
		this.id = id;
		this.pwd = pwd;
		this.nink = nink;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getNink() {
		return nink;
	}
	public void setNink(String nink) {
		this.nink = nink;
	}
	
	
}
